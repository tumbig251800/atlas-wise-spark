function weeklyOrHourlyLine(planType: "hourly" | "weekly", hours: number): string {
  return planType === "weekly"
    ? `แผนรายสัปดาห์: แยกเป็นชั่วโมง 1, 2, 3... จำนวน ${hours || 3} ชั่วโมง ไล่ระดับความท้าทาย Level 1 (ทบทวน) -> Level 2 (ประยุกต์) -> Level 3 (สร้างสรรค์)`
    : "แผนรายชั่วโมง: ออกแบบกิจกรรม 60 นาที แบ่งเป็น Warm-up (10 นาที) / Main (40 นาที) / Wrap-up (10 นาที)";
}

