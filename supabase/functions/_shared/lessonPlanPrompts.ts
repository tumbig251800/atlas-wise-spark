import type { LessonPlanGenerationMode, NormalizedLessonPlanRequest } from "./lessonPlanRequest.ts";

export interface LessonPlanSystemPromptParams {
  mode: LessonPlanGenerationMode;
  planType: "hourly" | "weekly";
  hours: number;
  gradeLevel: string;
  classroom: string;
  subject: string;
  learningUnit: string;
  topic: string;
  includeWorksheets: boolean;
}

function weeklyOrHourlyLine(planType: "hourly" | "weekly", hours: number): string {
  return planType === "weekly"
    ? `แผนรายสัปดาห์: แยกเป็นชั่วโมง 1, 2, 3... จำนวน ${hours || 3} ชั่วโมง ไล่ระดับความท้าทาย Level 1 (ทบทวน) -> Level 2 (ประยุกต์) -> Level 3 (สร้างสรรค์)`
    : "แผนรายชั่วโมง: ออกแบบกิจกรรม 50 นาที แบ่งเป็น Warm-up / Main / Wrap-up";
}

/** โหมดสะท้อนจากคาบ — อิง Teaching Logs / สถิติ Gap–Mastery */
