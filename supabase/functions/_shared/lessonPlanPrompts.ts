export function getAddonSystemPrompt(addonType: string | undefined): string {
  if (!addonType) return "สร้างเนื้อหาเสริมจากแผนการสอน";
  return ADDON_MAP[addonType] ?? "สร้างเนื้อหาเสริมจากแผนการสอน";
}

function formatSnapshotBlock(snapshot?: Record<string, unknown>): string {
  if (!snapshot || Object.keys(snapshot).length === 0) return "";
  try {
    return JSON.stringify(snapshot, null, 2);
  } catch {
    return "";
  }
}

/** User message สำหรับ Gemini — แยกโหมด reflection (Teaching Logs) กับ snapshot */
export function buildLessonPlanUserContent(v: NormalizedLessonPlanRequest): string {
  const modeLabel =
    v.mode === "context_snapshot" ? "บริบทคงที่ (Snapshot)" : "สะท้อนจากคาบ (Reflection)";
  const planLabel = v.planType === "weekly" ? "รายสัปดาห์" : "รายชั่วโมง";
  const unitLine = v.learningUnit.trim() ? `หน่วยการเรียน: ${v.learningUnit.trim()}\n` : "";
  const base =
    `ข้อมูลบริบท:\nชั้น: ${v.gradeLevel} ห้อง: ${v.classroom} วิชา: ${v.subject}\n` +
    unitLine +
    `หัวข้อ/เรื่องที่สอน: ${v.topic}\nประเภทแผน: ${planLabel}\nโหมดการสร้าง: ${modeLabel}\n\n`;

  const footer =
    `สำคัญ: สร้างเนื้อหาเฉพาะวิชา ${v.subject} เรื่อง ${v.topic}` +
    (v.learningUnit.trim() ? ` หน่วยการเรียน ${v.learningUnit.trim()}` : "") +
    ` สำหรับชั้น ${v.gradeLevel} ห้อง ${v.classroom} เท่านั้น ห้ามปนเนื้อหาวิชาอื่นโดยเด็ดขาด`;

  if (v.mode === "reflection") {
    return (
      base +
      `ข้อมูลจาก Teaching Logs ล่าสุด:\n${v.context}\n\n` +
      footer
    );
  }

  const snap = formatSnapshotBlock(v.snapshot);
  const snapshotBlock = snap
    ? `${snap}\n`
    : "(ยังไม่มีข้อมูล Snapshot โครงสร้าง — ใช้ข้อความบริบทด้านล่างเป็นหลัก)\n";
  const extraContext = v.context.trim()
    ? `ข้อความบริบทเพิ่มเติม:\n${v.context}\n\n`
    : "";
  return (
    base +
    `บริบทห้องเรียน (Snapshot / ที่ครูให้มา):\n${snapshotBlock}` +
    extraContext +
    footer
  );
}
