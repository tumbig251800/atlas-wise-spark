/**
 * unitScoreTemplate.ts
 * สร้างไฟล์ Excel "แบบบันทึกคะแนนหลังหน่วยการเรียนรู้" (K/P/A) ให้ครูดาวน์โหลด
 *
 * โครงสร้างต้องตรงกับ unitScoreExcelParser.ts เป๊ะ:
 * - แถว 2: วิชา=C2, ชั้น/ห้อง=F2, หน่วยที่=I2, ภาคเรียน=L2, วันที่สอบ=O2
 * - แถว 3: K เต็ม=C3, P เต็ม=F3, A เต็ม=I3
 * - แถว 7-36: นักเรียน — C=รหัส, D=ชื่อ, E-H=K รายข้อ(4), I-N=P รายข้อ(6), O=A
 *
 * คอลัมน์สรุป P-T (K รวม/P รวม/A รวม/รวม/PASS-STAY) เป็น "สูตร" คำนวณให้ครูอัตโนมัติ
 * — parser อ่านแค่ถึงคอลัมน์ O จึงไม่อ่าน P-T (ไม่กระทบการนำเข้า)
 * เกณฑ์ PASS = คะแนนรวม ≥ 60% ของ (K+P+A เต็ม)
 */
import * as XLSX from "xlsx";

const FIRST_DATA_ROW = 7;
const LAST_DATA_ROW = 36;

export function downloadUnitScoreTemplate() {
  const rows: (string | number)[][] = [];

  // แถว 1: หัวเอกสาร
  rows[0] = ["โรงเรียนวรนาถวิทยากำแพงเพชร — แบบบันทึกคะแนนหลังหน่วยการเรียนรู้"];

  // แถว 2: เมตาดาต้า (ป้ายที่ A/D/G/J/M, ค่าที่ C/F/I/L/O — เว้นว่างให้ครูกรอก)
  rows[1] = ["วิชา:", "", "", "ชั้น/ห้อง:", "", "", "หน่วยที่:", "", "", "ภาคเรียน:", "", "", "วันที่สอบ:", "", ""];

  // แถว 3: คะแนนเต็ม (C3=K, F3=P, I3=A) — เติม K=4, P=6 ตามจำนวนช่อง, A ให้ครูกรอก
  rows[2] = ["K เต็ม:", "", 4, "P เต็ม:", "", 6, "A เต็ม:", "", ""];

  // แถว 4: คำอธิบายย่อ
  rows[3] = ["📌 ข้อ 1–4 = K (ความรู้) · ข้อ 5–10 = P (ทักษะ) · A = เจตคติ — กรอกคะแนนรายข้อ K/P เป็น 0 หรือ 1 · คอลัมน์เทา (K รวม…PASS/STAY) คำนวณให้อัตโนมัติ"];

  // แถว 5: ป้ายกลุ่ม
  rows[4] = ["", "", "", "", "K — ความรู้", "", "", "", "P — ทักษะ/กระบวนการ", "", "", "", "", "", "A"];

  // แถว 6: หัวคอลัมน์ (รวมคอลัมน์สรุป P-T)
  rows[5] = ["#", "", "รหัสนักเรียน", "ชื่อ-นามสกุล", "ข้อ 1", "ข้อ 2", "ข้อ 3", "ข้อ 4", "ข้อ 5", "ข้อ 6", "ข้อ 7", "ข้อ 8", "ข้อ 9", "ข้อ 10", "A", "K รวม", "P รวม", "A รวม", "รวมทั้งหมด", "PASS/STAY"];

  // แถว 7-36: นักเรียน 30 แถว (ใส่เลขลำดับคอลัมน์ A)
  for (let i = 0; i < 30; i++) {
    rows[FIRST_DATA_ROW - 1 + i] = [i + 1];
  }

  const ws = XLSX.utils.aoa_to_sheet(rows);

  // ใส่สูตรคอลัมน์สรุป P-T สำหรับทุกแถวนักเรียน
  for (let r = FIRST_DATA_ROW; r <= LAST_DATA_ROW; r++) {
    ws[`P${r}`] = { t: "n", f: `SUM(E${r}:H${r})` };                 // K รวม
    ws[`Q${r}`] = { t: "n", f: `SUM(I${r}:N${r})` };                 // P รวม
    ws[`R${r}`] = { t: "n", f: `O${r}` };                            // A รวม
    ws[`S${r}`] = { t: "n", f: `SUM(P${r}:R${r})` };                 // รวมทั้งหมด
    ws[`T${r}`] = {
      t: "s",
      f: `IF(C${r}="","",IF(S${r}>=($C$3+$F$3+$I$3)*0.6,"PASS","STAY"))`,
    };
  }

  // ขยายขอบเขตชีตให้ครอบคลุมคอลัมน์สูตร (A1:T36)
  ws["!ref"] = `A1:T${LAST_DATA_ROW}`;

  ws["!cols"] = [
    { wch: 4 }, { wch: 2 }, { wch: 12 }, { wch: 26 },
    { wch: 6 }, { wch: 6 }, { wch: 6 }, { wch: 6 },
    { wch: 6 }, { wch: 6 }, { wch: 6 }, { wch: 6 }, { wch: 6 }, { wch: 6 },
    { wch: 8 },
    { wch: 7 }, { wch: 7 }, { wch: 7 }, { wch: 10 }, { wch: 11 },
  ];

  // ── Sheet 2: คำแนะนำการใช้งาน ────────────────────────────────────────────
  const guide: string[][] = [
    ["โรงเรียนวรนาถวิทยากำแพงเพชร — วิธีกรอกแบบบันทึกคะแนนหลังหน่วย"],
    [""],
    ["1. กรอกข้อมูลหน่วยในแถวที่ 2: วิชา / ชั้น-ห้อง / หน่วยที่ / ภาคเรียน / วันที่สอบ"],
    ["   - ชั้น/ห้อง ใส่รูปแบบ ป.X/ห้อง เช่น ป.1/2 หรือ ป.3/KBW"],
    ["   - ภาคเรียน ใส่ 1 หรือ 2 (หรือ 2569-1)"],
    ["2. กรอกคะแนนเต็มในแถวที่ 3: K เต็ม (ปกติ 4) / P เต็ม (ปกติ 6) / A เต็ม (เช่น 2)"],
    ["   - ถ้าใช้ข้อน้อยกว่า ปรับคะแนนเต็มให้ตรงจำนวนข้อที่ใช้"],
    ["3. กรอกรหัสนักเรียน (คอลัมน์ C, ตัวเลข 4 หลัก) และชื่อ-นามสกุล (คอลัมน์ D)"],
    ["4. กรอกคะแนนรายข้อ K (ข้อ 1–4) และ P (ข้อ 5–10) เป็น 0 หรือ 1"],
    ["5. กรอกคะแนน A (เจตคติ) เป็นตัวเลข"],
    ["6. คอลัมน์ K รวม / P รวม / A รวม / รวมทั้งหมด / PASS-STAY คำนวณให้อัตโนมัติ — ไม่ต้องกรอก"],
    ["7. บันทึกเป็นไฟล์ .xlsx แล้วนำเข้าที่เมนู ATLAS > บันทึกคะแนนหน่วย > นำเข้าคะแนน K/P/A"],
    [""],
    ["เกณฑ์ PASS / STAY"],
    ["PASS = คะแนนรวม ≥ 60% ของคะแนนเต็มทั้งหมด (K+P+A)"],
    ["STAY = คะแนนรวม < 60% ต้องเข้ารับการซ่อมเสริม"],
    [""],
    ["⚠ ห้ามย้าย/ลบ ป้ายและช่องในแถว 2–3 และห้ามลบแถวหัวตาราง (แถว 1–6) — ระบบอ่านตามตำแหน่งนี้"],
  ];
  const wsGuide = XLSX.utils.aoa_to_sheet(guide);
  wsGuide["!cols"] = [{ wch: 95 }];

  // ── รวมเป็นไฟล์ — "แบบบันทึกคะแนน" ต้องเป็น sheet แรก (parser อ่าน SheetNames[0]) ──
  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, "แบบบันทึกคะแนน");
  XLSX.utils.book_append_sheet(wb, wsGuide, "คำแนะนำการใช้งาน");

  XLSX.writeFile(wb, "แบบบันทึกคะแนนหลังหน่วย-template.xlsx");
}
